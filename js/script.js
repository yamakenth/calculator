// add two numbers 
// take in two numbers 
// return sum
function add(a, b) {
  return a + b;
}

// subtract second param from first param 
// take in two numbers 
// return difference 
function subtract(a, b) {
  return a - b;
}

// mulitply two numbers 
// take in two numbers 
// return product 
function multiply(a, b) {
  return a * b;
}

// divide first param by second param
// take in two numbers 
// return product or '#DIV/0!'
function divide(a, b) {
  return (b === 0) ? '#DIV/0!' : a / b;
}